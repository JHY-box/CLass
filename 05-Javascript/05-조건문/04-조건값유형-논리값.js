//boolean형 변수를 조건식으로 사용하는 경우

let isKorean = true;
//let isKorean = false;

if (isKorean === true)  console.log("한국사람입니다...(1)");
if (isKorean === false)  console.log("한국사람이 아닙니다...(2)");
if (isKorean !== true)  console.log("한국사람이 아닙니다...(3)");
if (isKorean !== false)  console.log("한국사람입니다...(4)");
if (isKorean)  console.log("한국사람입니다...(5)");
if (!isKorean)  console.log("한국사람이 아닙니다...(6)");